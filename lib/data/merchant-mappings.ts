// Simple lookup function
export function findMerchantAnzsicCode(merchantName: string): string | null {
  const key = merchantName.toLowerCase().trim()
  return MERCHANT_MAPPINGS[key] || null
}

// Get all merchants as simple array
export function getAllMerchants(): Array<{ merchantName: string; anzsicCode: string }> {
  return Object.entries(MERCHANT_MAPPINGS).map(([merchantName, anzsicCode]) => ({
    merchantName,
    anzsicCode,
  }))
}

// Search merchants by partial name
export function searchMerchantsByName(query: string): Array<{ merchantName: string; anzsicCode: string }> {
  const searchTerm = query.toLowerCase()
  const results: Array<{ merchantName: string; anzsicCode: string }> = []

  for (const [merchantName, anzsicCode] of Object.entries(MERCHANT_MAPPINGS)) {
    if (merchantName.includes(searchTerm)) {
      results.push({ merchantName, anzsicCode })
    }
  }

  return results
}
